test(
  'WaterTest',

  [
    'ephox.snooker.activate.Water'
  ],

  function (Water) {
    var min = 10;
    var check = function (expected, input, column, step) {
      var actual = Water.water(input, column, step, min);
      console.log('Actual: ', actual);
      assert.eq(expected, actual);
    };

    check([80, 70, 250, 100], [100, 50, 250, 100], 0, -20);
    check([10, 140, 250, 100], [100, 50, 250, 100], 0, -200);
    check([120, 30, 250, 100], [100, 50, 250, 100], 0, 20);
    check([180, 10, 250, 100], [100, 50, 250, 100], 0, 80);

    check([100, 20, 280, 100], [100, 50, 250, 100], 1, -30);
    check([100, 10, 290, 100], [100, 50, 250, 100], 1, -100);
    check([100, 80, 220, 100], [100, 50, 250, 100], 1, 30);
    check([100, 450, 10, 100], [100, 50, 250, 100], 1, 400);

    check([100, 50, 230, 120], [100, 50, 250, 100], 2, -20);
    check([100, 50, 10, 340], [100, 50, 250, 100], 2, -300);
    check([100, 50, 270, 80], [100, 50, 250, 100], 2, 20);
    check([100, 50, 400, 10], [100, 50, 250, 100], 2, 150);

    check([100, 50, 250, 150], [100, 50, 250, 100], 3, 50);
    check([100, 50, 250, 200], [100, 50, 250, 100], 3, 100);
    check([100, 50, 250, 50], [100, 50, 250, 100], 3, -50);
    check([100, 50, 250, 10], [100, 50, 250, 100], 3, -150);

    // check([127, 127, 127], [127, 127, 127], 0, 100);
    // widths [127, 127, 127] column 0 step 100 min: 10 

  }
);
